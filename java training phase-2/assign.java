public class assign {
    public static void main(String[] args)
    {
        int a=10,b=30,c=20;
        b+=a;
        System.out.println(b);
        c-=b;
        System.out.println(c);
        b*=a;
        System.out.println(b);
        b/=a;
        System.out.println(b);
        b%=a;
        System.out.println(b);
    }
}
